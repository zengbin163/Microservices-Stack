#!/bin/bash
cd /home/admin/deploy/
rm -rf *maven* *classes* *sources* *original*
if [ ! -f "member-1.0.0-SNAPSHOT.jar" ];then
  echo "文件不存在"
else
  mv -f member-1.0.0-SNAPSHOT.jar member.jar
fi
echo "========================================================================================================================================================================================================================================"
echo "=======================================================================================================================法苗网+=========================================================================================================="
echo "========================================================================================================================================================================================================================================"
export JAVA_HOME=/usr/local/java/jdk1.8.0_191
export CLASSPATH=$JAVA_HOME/jre/lib:$JAVA_HOME/lib
export PATH=$PATH:$JAVA_HOME/bin
java -version
pid=`netstat -anp|grep 8085|awk '{printf $7}'|cut -d/ -f1`
echo "java进程号$pid"
kill -9 $pid
java -jar member.jar